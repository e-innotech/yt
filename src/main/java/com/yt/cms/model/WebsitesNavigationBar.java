package com.yt.cms.model;

public class WebsitesNavigationBar {
    private Integer id;

    private Integer websitesId;

    private Integer navigationBarId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getWebsitesId() {
        return websitesId;
    }

    public void setWebsitesId(Integer websitesId) {
        this.websitesId = websitesId;
    }

	public Integer getNavigationBarId() {
		return navigationBarId;
	}

	public void setNavigationBarId(Integer navigationBarId) {
		this.navigationBarId = navigationBarId;
	}

}